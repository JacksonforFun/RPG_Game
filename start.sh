#!/bin/bash

# Скрипт для запуска Сказочника (сервер + клиент)

echo "🎭 Запуск Сказочника..."

# Проверяем, установлены ли зависимости сервера
if [ ! -d "server/node_modules" ]; then
    echo "📦 Установка зависимостей сервера..."
    cd server && npm install && cd ..
fi

# Запускаем сервер в фоне
echo "🌐 Запуск сервера на порту 3001..."
cd server && npm start &
SERVER_PID=$!
cd ..

# Ждём запуска сервера
sleep 2

# Запускаем клиент
echo "🎮 Запуск клиента..."
npm run dev

# При завершении клиента — убиваем сервер
kill $SERVER_PID 2>/dev/null
echo "👋 Сказочник завершён"
